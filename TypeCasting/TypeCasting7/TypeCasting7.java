// TypeCasting Float

public class TypeCasting7 {
    public static void main(String[] args) {
        float a = 69f;

        // converting float to long
        long b = (long)a;

        // converting float to int 
        int c = (int) a;

        // converting float to char 
        char d = (char) a;

        // converting float to short 
        short e = (short)a;

        // convertinh float to byte
        byte f = (byte)a;

        System.out.println("This is float = " + a);
        System.out.println("This is long = " + b);
        System.out.println("This is int = " + c);
        System.out.println("This is char = " + d);
        System.out.println("This is short = " + e);
        System.out.println("This is byte = " + f);
    }    
}
