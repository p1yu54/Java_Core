// TypeCasting integer from long to double

public class TypeCasting3 {
    public static void main(String[] args) {
        int a = 69;

        // converting int to long
        long b = a;

        // coverting int  to float 
        float c = a;

        // converting int to double 
        double d = a;

        System.out.println("This is integer = " + a);
        System.out.println("This is long = " + b);
        System.out.println("This is float = " + c);
        System.out.println("This is double = " + d);
    }    
}
