// TypeCasting long to float and double

public class TypeCasting4 {
    public static void main(String[] args) {
        long a = 69;
        
        // converting long to float 
        float b = a;

        // converting long to double 
        double c = a;

        System.out.println("This is long = " + a);
        System.out.println("This is float = " + b);
        System.out.println("This is double = " + c);
    }
}
