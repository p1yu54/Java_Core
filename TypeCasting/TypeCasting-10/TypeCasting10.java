// TypeCasting char

public class TypeCasting10 {
    public static void main(String[] args) {
         char a = 'c';
 
         // converting char to short
         short b = (short) a;
 
         // converting char to byte
         byte c = (byte) a;
 
         System.out.println("This is char = " + a);
         System.out.println("This is short = " + b);
         System.out.println("This is byte = " + c);
    }
}
